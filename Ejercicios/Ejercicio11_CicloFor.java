package Ejercicios;

public class Ejercicio11_CicloFor {
    public static void main(String[] args) {

        // CICLO FOR

        for(int cont =0; cont<=10; cont++){
            // el contador lo inicio adentro del for --> for( contador = __ )
            // separo con --> ;
            // cont++ es para incrementar 1, es lo mismo que --> cont +1

            System.out.println("Estoy dando vueltas con FOR, estoy en la vuelta: " + cont);
            // esto imprime desde la vuelta 0 hasta llegar a 10. Incrementa de 1 en 1.
        }




    }
}
